#include "main.h"


/**
 * handle_plus - handle plus flag
 * @num: number
 */



void handle_plus(int num)
{
	if (num >= 0)
		_putchar('+');
	else
		_putchar('-');
}






/**
 * handle_space - handle space flag
 * @num: number
 */



void handle_space(int num)
{
	if (num >= 0)
		_putchar(' ');
	else
		_putchar('-');
}
